package com.liferay.change.tracking.engine.uad.anonymizer;

import com.liferay.change.tracking.engine.model.CTECollection;
import com.liferay.change.tracking.engine.service.CTECollectionLocalService;
import com.liferay.change.tracking.engine.uad.constants.ChangeTrackingEngineUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the cte collection UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link CTECollectionUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseCTECollectionUADAnonymizer
	extends DynamicQueryUADAnonymizer<CTECollection> {
	@Override
	public void autoAnonymize(CTECollection cteCollection, long userId,
		User anonymousUser) throws PortalException {
		if (cteCollection.getUserId() == userId) {
			cteCollection.setUserId(anonymousUser.getUserId());
			cteCollection.setUserName(anonymousUser.getFullName());
		}

		if (cteCollection.getStatusByUserId() == userId) {
			cteCollection.setStatusByUserId(anonymousUser.getUserId());
			cteCollection.setStatusByUserName(anonymousUser.getFullName());
		}

		cteCollectionLocalService.updateCTECollection(cteCollection);
	}

	@Override
	public void delete(CTECollection cteCollection) throws PortalException {
		cteCollectionLocalService.deleteCTECollection(cteCollection);
	}

	@Override
	public Class<CTECollection> getTypeClass() {
		return CTECollection.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return cteCollectionLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return ChangeTrackingEngineUADConstants.USER_ID_FIELD_NAMES_CTE_COLLECTION;
	}

	@Reference
	protected CTECollectionLocalService cteCollectionLocalService;
}