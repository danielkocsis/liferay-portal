package com.liferay.change.tracking.engine.uad.anonymizer;

import com.liferay.change.tracking.engine.model.CTEEntry;
import com.liferay.change.tracking.engine.service.CTEEntryLocalService;
import com.liferay.change.tracking.engine.uad.constants.ChangeTrackingEngineUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the cte entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link CTEEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseCTEEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<CTEEntry> {
	@Override
	public void autoAnonymize(CTEEntry cteEntry, long userId, User anonymousUser)
		throws PortalException {
		if (cteEntry.getUserId() == userId) {
			cteEntry.setUserId(anonymousUser.getUserId());
			cteEntry.setUserName(anonymousUser.getFullName());
		}

		cteEntryLocalService.updateCTEEntry(cteEntry);
	}

	@Override
	public void delete(CTEEntry cteEntry) throws PortalException {
		cteEntryLocalService.deleteCTEEntry(cteEntry);
	}

	@Override
	public Class<CTEEntry> getTypeClass() {
		return CTEEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return cteEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return ChangeTrackingEngineUADConstants.USER_ID_FIELD_NAMES_CTE_ENTRY;
	}

	@Reference
	protected CTEEntryLocalService cteEntryLocalService;
}